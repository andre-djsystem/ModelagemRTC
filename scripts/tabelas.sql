CREATE TABLE NCM_NBS_CCLASSTRIB (
  NCM_NBS         VARCHAR(10) NOT NULL,
  REGRA_RTC   VARCHAR(50) NOT NULL,
  ID_PARTICIPANTE INTEGER DEFAULT 0 NOT NULL,
